
public class Line extends Upgrades {
	public Line(int lineNum) {
			
			if(lineNum == 1) {
				this.setName("Great Line");
				this.setPrice(700);
				this.setfEncounter(0);
				this.setfCapture(10);
			}
			
			if(lineNum == 2) {
				this.setName("Ultra Line");
				this.setPrice(800);
				this.setfEncounter(0);
				this.setfCapture(15);
			}
			
			if(lineNum == 3) {
				this.setName("Master Line");
				this.setPrice(900);
				this.setfEncounter(0);
				this.setfCapture(25);
			}
		}
		
	}


