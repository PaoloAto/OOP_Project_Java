
public abstract class Upgrades {
	private String name;
	private float price;
	private float fCapture;
	private float fEncounter;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public float getfCapture() {
		return fCapture;
	}
	public void setfCapture(float fCapture) {
		this.fCapture = fCapture;
	}
	public float getfEncounter() {
		return fEncounter;
	}
	public void setfEncounter(float fEncounter) {
		this.fEncounter = fEncounter;
	}

	
}
