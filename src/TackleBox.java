
public class TackleBox {
private String sName;
private float fCost;
private int nCasts;

public TackleBox(String sName, float fCost, int nCasts) {
	this.sName = sName;
	this.fCost = fCost;
	this.nCasts = nCasts;
}

public String getsName() {
	return sName;
}

public float getfCost() {
	return fCost;
}

public int getnCasts() {
	return nCasts;
}

	
}
